public class OutOO {
	
	Object obj1;
	Object obj2;
	
	public OutOO(Object obj1, Object obj2) {
		super();
		this.obj1 = obj1;
		this.obj2 = obj2;
	}

	public Object get_obj1() {
		return obj1;
	}

	public void set_obj1(Object obj) {
		this.obj1 = obj;
	}

	public Object get_obj2() {
		return obj2;
	}

	public void set_obj2(Object obj) {
		this.obj2 = obj;
	}
	
	

}
