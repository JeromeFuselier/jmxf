package uk.ac.liv.ffmpeg;

public class OptionValue {

	int int_arg;
	boolean bool_arg;
	String str_arg;
	float float_arg;
    long int64_arg;
   
    String func_arg;

	public OptionValue(String func_arg) {
		super();
		this.func_arg = func_arg;
	}

	public int get_int_arg() {
		return int_arg;
	}

	public void set_int_arg(int int_arg) {
		this.int_arg = int_arg;
	}

	public boolean get_bool_arg() {
		return bool_arg;
	}

	public void set_bool_arg(boolean bool_arg) {
		this.bool_arg = bool_arg;
	}

	public String get_str_arg() {
		return str_arg;
	}

	public void set_str_arg(String str_arg) {
		this.str_arg = str_arg;
	}

	public float get_float_arg() {
		return float_arg;
	}

	public void set_float_arg(float float_arg) {
		this.float_arg = float_arg;
	}

	public long get_int64_arg() {
		return int64_arg;
	}

	public void set_int64_arg(long int64_arg) {
		this.int64_arg = int64_arg;
	}

	public String get_func_arg() {
		return func_arg;
	}

	public void set_func_arg(String func_arg) {
		this.func_arg = func_arg;
	}
    
    
   
   
}
