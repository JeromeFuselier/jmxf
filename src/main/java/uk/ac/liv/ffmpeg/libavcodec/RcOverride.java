package uk.ac.liv.ffmpeg.libavcodec;

public class RcOverride {
	
	int start_frame;
    int end_frame;
    int qscale; // If this is 0 then quality_factor will be used instead.
    float quality_factor;
    
	public int get_start_frame() {
		return start_frame;
	}
	public void set_start_frame(int start_frame) {
		this.start_frame = start_frame;
	}
	public int get_end_frame() {
		return end_frame;
	}
	public void set_end_frame(int end_frame) {
		this.end_frame = end_frame;
	}
	public int get_qscale() {
		return qscale;
	}
	public void set_qscale(int qscale) {
		this.qscale = qscale;
	}
	public float get_quality_factor() {
		return quality_factor;
	}
	public void set_quality_factor(float quality_factor) {
		this.quality_factor = quality_factor;
	}
    
    
}
